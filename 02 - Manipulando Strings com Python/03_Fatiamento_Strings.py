'''
Fatiamento de strings é uma técnica utilizada para retornar
substrings (partes da string original), informando inicio (start),
fim (stop) e passo (step): 

[start: stop[,step]].

'''
#%%
nome = "Felipe de Moraes Aparecido"

print(nome[0])
print(nome[:9])
print(nome[10:])
print(nome[10:16])
print(nome[10:20:3])
print(nome[:])
print(nome[::-1])
#%%