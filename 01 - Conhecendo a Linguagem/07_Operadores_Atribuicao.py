'''
Operadores de Atribuição

São operadores utilizados para definir o valor inicial ou
sobrescrever o valor de uma variável.

Atribuição simples: =
    saldo = 500

Atribuição com adição: +=
    saldo += 500

Atribuição com subtração: -=
    saldo -= 500

outros seguem a mesma lógica: 
    saldo /= 100
    saldo //= 100
    saldo **= 100
    saldo *= 100

    
'''
#%% 
saldo = 500

print(saldo)

saldo += 500
print(saldo)

saldo -= 500
print(saldo)

saldo /= 100
print(saldo)

saldo //= 100
print(saldo)

saldo **= 100
print(saldo)

saldo *= 100
print(saldo)


